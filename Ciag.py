def wypisz_ciag(ciag):
    for liczba in ciag:
        print(liczba, end=' ')

# a) 7-elementowy ciąg liczb całkowitych postaci (-4;-1;2;5;8;11;14)
ciag_a = [-4, -1, 2, 5, 8, 11, 14]
wypisz_ciag(ciag_a)

# b) 5-elementowy ciąg liczb całkowitych postaci (-4;-1;5;11;14)
ciag_b = [-4, -1, 5, 11, 14]
wypisz_ciag(ciag_b)

# c) 7-elementowy ciąg liczb całkowitych postaci (12;8;4;0;-4;-8;-12)
ciag_c = [12, 8, 4, 0, -4, -8, -12]
wypisz_ciag(ciag_c)

# d) 6-elementowy ciąg liczb całkowitych postaci (12;8;4;-4;-8;-12)
ciag_d = [12, 8, 4, -4, -8, -12]
wypisz_ciag(ciag_d)
